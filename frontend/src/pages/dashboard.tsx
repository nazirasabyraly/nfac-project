import React, { useEffect, useState } from 'react'
import { useNavigate } from 'react-router-dom'
import { API_BASE_URL } from '../config'
import Chat from '../components/Chat'

interface MusicAnalysis {
  mood: {
    valence: number;
    energy: number;
    danceability: number;
    tempo: number;
  };
  preferences: {
    favorite_artists: string[];
    total_tracks_analyzed: number;
  };
  overall_mood: string;
}

interface Track {
  name: string;
  artist: string;
  image: string;
  valence: number;
  energy: number;
  danceability: number;
}

const Dashboard = () => {
  const navigate = useNavigate()
  const [userProfile, setUserProfile] = useState<any>(null)
  const [musicAnalysis, setMusicAnalysis] = useState<MusicAnalysis | null>(null)
  const [topTracks, setTopTracks] = useState<Track[]>([])
  const [loading, setLoading] = useState(true)
  const [hasData, setHasData] = useState(false)
  const [error, setError] = useState<string | null>(null)
  const [activeTab, setActiveTab] = useState<'analysis' | 'chat'>('analysis')
  const [theme, setTheme] = useState(() => localStorage.getItem('theme') || 'light')

  useEffect(() => {
    document.documentElement.classList.remove('theme-light', 'theme-dark')
    document.documentElement.classList.add(theme === 'dark' ? 'theme-dark' : 'theme-light')
    localStorage.setItem('theme', theme)
  }, [theme])

  useEffect(() => {
    // Check for token in URL parameters (from Spotify OAuth redirect)
    const urlParams = new URLSearchParams(window.location.search)
    const tokenFromUrl = urlParams.get('token')
    const spotifyIdFromUrl = urlParams.get('spotify_id')
    
    if (tokenFromUrl) {
      console.log('üéØ Token found in URL, saving to localStorage')
      localStorage.setItem('spotify_token', tokenFromUrl)
      if (spotifyIdFromUrl) {
        localStorage.setItem('spotify_id', spotifyIdFromUrl)
      }
      // Clean up URL
      window.history.replaceState({}, document.title, window.location.pathname)
    }

    const authToken = localStorage.getItem('auth_token')
    const spotifyToken = localStorage.getItem('spotify_token')
    
    if (!authToken && !spotifyToken) {
      navigate('/')
      return
    }

    const fetchData = async () => {
      try {
        const headers: Record<string, string> = {
          'Content-Type': 'application/json'
        }

        // –ò—Å–ø–æ–ª—å–∑—É–µ–º auth_token –¥–ª—è API –∑–∞–ø—Ä–æ—Å–æ–≤, –µ—Å–ª–∏ –µ—Å—Ç—å
        if (authToken) {
          headers['Authorization'] = `Bearer ${authToken}`
        } else if (spotifyToken) {
          headers['Authorization'] = `Bearer ${spotifyToken}`
        }

        // –ü–æ–ª—É—á–∞–µ–º –ø—Ä–æ—Ñ–∏–ª—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        const profileResponse = await fetch(`${API_BASE_URL}/media/profile`, { headers })
        if (profileResponse.ok) {
          const profile = await profileResponse.json()
          setUserProfile(profile)
        }

        // –ü–æ–ª—É—á–∞–µ–º –∞–Ω–∞–ª–∏–∑ –º—É–∑—ã–∫–∞–ª—å–Ω—ã—Ö –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏–π
        const analysisResponse = await fetch(`${API_BASE_URL}/media/analysis`, { headers })
        if (analysisResponse.ok) {
          const analysis = await analysisResponse.json()
          if (analysis.error) {
            setError(analysis.error)
          } else {
            setMusicAnalysis(analysis)
            setHasData(true)
          }
        }

        // –ü–æ–ª—É—á–∞–µ–º —Ç–æ–ø —Ç—Ä–µ–∫–∏
        const tracksResponse = await fetch(`${API_BASE_URL}/media/top-tracks?limit=5`, { headers })
        if (tracksResponse.ok) {
          const tracksData = await tracksResponse.json()
          if (tracksData.tracks && tracksData.tracks.length > 0) {
            setTopTracks(tracksData.tracks)
            setHasData(true)
          }
        }

      } catch (error) {
        console.error('Error fetching data:', error)
        setError('–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–≥—Ä—É–∑–∫–µ –¥–∞–Ω–Ω—ã—Ö')
      } finally {
        setLoading(false)
      }
    }

    fetchData()
  }, [navigate])

  const handleLogout = () => {
    localStorage.removeItem('auth_token')
    localStorage.removeItem('spotify_token')
    localStorage.removeItem('user_info')
    navigate('/')
  }

  const getMoodEmoji = (mood: string) => {
    switch (mood) {
      case '–ü–æ–∑–∏—Ç–∏–≤–Ω–æ–µ': return 'üòä'
      case '–ù–µ–π—Ç—Ä–∞–ª—å–Ω–æ–µ': return 'üòê'
      case '–ú–µ–ª–∞–Ω—Ö–æ–ª–∏—á–Ω–æ–µ': return 'üòî'
      default: return 'üéµ'
    }
  }

  const formatPercentage = (value: number) => {
    return Math.round(value * 100)
  }

  if (loading) {
    return (
      <div style={{ textAlign: 'center', minHeight: '100vh', background: 'var(--bg)', color: 'var(--text)', display: 'flex', flexDirection: 'column', justifyContent: 'center', alignItems: 'center' }}>
        <h2>–ó–∞–≥—Ä—É–∑–∫–∞...</h2>
        <p>–ê–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º –≤–∞—à –º—É–∑—ã–∫–∞–ª—å–Ω—ã–π –≤–∫—É—Å ‚è≥</p>
      </div>
    )
  }

  return (
    <div style={{ minHeight: '100vh', background: 'var(--bg)', color: 'var(--text)', width: '100vw', boxSizing: 'border-box', padding: '20px' }}>
      <div style={{ display: 'flex', justifyContent: 'space-between', alignItems: 'center', marginBottom: '30px' }}>
        <h1>üéµ Aivi Dashboard</h1>
        <div style={{ display: 'flex', alignItems: 'center' }}>
          <button 
            onClick={handleLogout}
            style={{
              padding: '10px 20px',
              background: '#1DB954',
              color: 'white',
              border: 'none',
              borderRadius: '5px',
              cursor: 'pointer'
            }}
          >
            –í—ã–π—Ç–∏
          </button>
          <button
            className="theme-toggle-btn"
            onClick={() => setTheme(theme === 'dark' ? 'light' : 'dark')}
            title={theme === 'dark' ? '–°–≤–µ—Ç–ª–∞—è —Ç–µ–º–∞' : '–¢—ë–º–Ω–∞—è —Ç–µ–º–∞'}
          >
            {theme === 'dark' ? 'üåû' : 'üåô'}
          </button>
        </div>
      </div>

      {/* –¢–∞–±—ã –¥–ª—è –ø–µ—Ä–µ–∫–ª—é—á–µ–Ω–∏—è */}
      <div style={{ 
        display: 'flex', 
        marginBottom: '20px', 
        borderBottom: '2px solid #e1e5e9',
        background: 'white',
        borderRadius: '10px 10px 0 0',
        overflow: 'hidden'
      }}>
        <button
          onClick={() => setActiveTab('analysis')}
          style={{
            flex: 1,
            padding: '15px 20px',
            background: activeTab === 'analysis' ? '#667eea' : '#f8f9fa',
            color: activeTab === 'analysis' ? 'white' : '#333',
            border: 'none',
            cursor: 'pointer',
            fontSize: '16px',
            fontWeight: activeTab === 'analysis' ? '600' : '400',
            transition: 'all 0.2s ease'
          }}
        >
          üìä –ê–Ω–∞–ª–∏–∑ –º—É–∑—ã–∫–∏
        </button>
        <button
          onClick={() => setActiveTab('chat')}
          style={{
            flex: 1,
            padding: '15px 20px',
            background: activeTab === 'chat' ? '#667eea' : '#f8f9fa',
            color: activeTab === 'chat' ? 'white' : '#333',
            border: 'none',
            cursor: 'pointer',
            fontSize: '16px',
            fontWeight: activeTab === 'chat' ? '600' : '400',
            transition: 'all 0.2s ease'
          }}
        >
          ü§ñ –ò–ò-—á–∞—Ç
        </button>
      </div>

      {/* –ö–æ–Ω—Ç–µ–Ω—Ç —Ç–∞–±–æ–≤ */}
      {activeTab === 'analysis' && (
        <div>
          {/* –ü—Ä–æ—Ñ–∏–ª—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è */}
          {userProfile && (
            <div className="dashboard-block fade-in slide-up" style={{ background: 'var(--bg-card)', padding: '20px', borderRadius: '10px', marginBottom: '20px' }}>
              <h2>üë§ –ü—Ä–æ—Ñ–∏–ª—å</h2>
              <p><strong>–ò–º—è:</strong> {userProfile.display_name}</p>
              <p><strong>Email:</strong> {userProfile.email}</p>
              <p><strong>–°—Ç—Ä–∞–Ω–∞:</strong> {userProfile.country}</p>
            </div>
          )}

          {/* –°–æ–æ–±—â–µ–Ω–∏–µ –æ –Ω–µ–¥–æ—Å—Ç–∞—Ç–∫–µ –¥–∞–Ω–Ω—ã—Ö */}
          {!hasData && !loading && (
            <div className="dashboard-block fade-in slide-up" style={{ background: '#fff3cd', border: '1px solid #ffeaa7', padding: '20px', borderRadius: '10px', marginBottom: '20px', color: '#222', width: '100%' }}>
              <h2>üéµ –î–æ–±—Ä–æ –ø–æ–∂–∞–ª–æ–≤–∞—Ç—å –≤ Aivi!</h2>
              <p>–ü–æ—Ö–æ–∂–µ, —á—Ç–æ –≤ –≤–∞—à–µ–º Spotify –∞–∫–∫–∞—É–Ω—Ç–µ –ø–æ–∫–∞ –º–∞–ª–æ –¥–∞–Ω–Ω—ã—Ö –¥–ª—è –∞–Ω–∞–ª–∏–∑–∞. –í–æ—Ç —á—Ç–æ –º–æ–∂–Ω–æ —Å–¥–µ–ª–∞—Ç—å:</p>
              <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fit, minmax(250px, 1fr))', gap: '15px', marginTop: '20px' }}>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px', border: '1px solid #e9ecef', color: '#222' }}>
                  <h4>üéº –°–ª—É—à–∞–π—Ç–µ –±–æ–ª—å—à–µ –º—É–∑—ã–∫–∏</h4>
                  <p>–ß–µ–º –±–æ–ª—å—à–µ —Ç—Ä–µ–∫–æ–≤ –≤—ã —Å–ª—É—à–∞–µ—Ç–µ –≤ Spotify, —Ç–µ–º —Ç–æ—á–Ω–µ–µ –±—É–¥–µ—Ç –∞–Ω–∞–ª–∏–∑ –≤–∞—à–µ–≥–æ –≤–∫—É—Å–∞.</p>
                </div>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px', border: '1px solid #e9ecef', color: '#222' }}>
                  <h4>‚ù§Ô∏è –õ–∞–π–∫–∞–π—Ç–µ —Ç—Ä–µ–∫–∏</h4>
                  <p>–°—Ç–∞–≤—å—Ç–µ –ª–∞–π–∫–∏ –ª—é–±–∏–º—ã–º –ø–µ—Å–Ω—è–º, —á—Ç–æ–±—ã –º—ã –ª—É—á—à–µ –ø–æ–Ω–∏–º–∞–ª–∏ –≤–∞—à–∏ –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏—è.</p>
                </div>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px', border: '1px solid #e9ecef', color: '#222' }}>
                  <h4>üì± –°–æ–∑–¥–∞–≤–∞–π—Ç–µ –ø–ª–µ–π–ª–∏—Å—Ç—ã</h4>
                  <p>–°–æ–∑–¥–∞–≤–∞–π—Ç–µ –ø–ª–µ–π–ª–∏—Å—Ç—ã –ø–æ–¥ —Ä–∞–∑–Ω—ã–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏—è –∏ –∞–∫—Ç–∏–≤–Ω–æ—Å—Ç–∏.</p>
                </div>
              </div>
            </div>
          )}

          {/* –ê–Ω–∞–ª–∏–∑ –º—É–∑—ã–∫–∞–ª—å–Ω—ã—Ö –ø—Ä–µ–¥–ø–æ—á—Ç–µ–Ω–∏–π */}
          {musicAnalysis && hasData && (
            <div className="dashboard-block fade-in slide-up" style={{ background: 'var(--bg-card)', padding: '20px', borderRadius: '10px', marginBottom: '20px' }}>
              <h2>üéº –ê–Ω–∞–ª–∏–∑ –º—É–∑—ã–∫–∞–ª—å–Ω–æ–≥–æ –≤–∫—É—Å–∞</h2>
              
              <div style={{ display: 'flex', alignItems: 'center', marginBottom: '15px' }}>
                <span style={{ fontSize: '24px', marginRight: '10px' }}>
                  {getMoodEmoji(musicAnalysis.overall_mood)}
                </span>
                <h3>–û–±—â–µ–µ –Ω–∞—Å—Ç—Ä–æ–µ–Ω–∏–µ: {musicAnalysis.overall_mood}</h3>
              </div>

              <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fit, minmax(200px, 1fr))', gap: '15px', marginBottom: '20px' }}>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px' }}>
                  <h4>üòä –ü–æ–∑–∏—Ç–∏–≤–Ω–æ—Å—Ç—å</h4>
                  <p style={{ fontSize: '24px', fontWeight: 'bold', color: '#1DB954' }}>
                    {formatPercentage(musicAnalysis.mood.valence)}%
                  </p>
                </div>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px' }}>
                  <h4>‚ö° –≠–Ω–µ—Ä–≥–∏—á–Ω–æ—Å—Ç—å</h4>
                  <p style={{ fontSize: '24px', fontWeight: 'bold', color: '#1DB954' }}>
                    {formatPercentage(musicAnalysis.mood.energy)}%
                  </p>
                </div>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px' }}>
                  <h4>üíÉ –¢–∞–Ω—Ü–µ–≤–∞–ª—å–Ω–æ—Å—Ç—å</h4>
                  <p style={{ fontSize: '24px', fontWeight: 'bold', color: '#1DB954' }}>
                    {formatPercentage(musicAnalysis.mood.danceability)}%
                  </p>
                </div>
                <div style={{ background: 'white', padding: '15px', borderRadius: '8px' }}>
                  <h4>üéµ –¢–µ–º–ø</h4>
                  <p style={{ fontSize: '24px', fontWeight: 'bold', color: '#1DB954' }}>
                    {Math.round(musicAnalysis.mood.tempo)} BPM
                  </p>
                </div>
              </div>

              <div style={{ background: 'white', padding: '15px', borderRadius: '8px' }}>
                <h4>üé§ –õ—é–±–∏–º—ã–µ –∏—Å–ø–æ–ª–Ω–∏—Ç–µ–ª–∏</h4>
                <p>{musicAnalysis.preferences.favorite_artists.slice(0, 5).join(', ')}</p>
                <p style={{ fontSize: '12px', color: '#666' }}>
                  –ü—Ä–æ–∞–Ω–∞–ª–∏–∑–∏—Ä–æ–≤–∞–Ω–æ —Ç—Ä–µ–∫–æ–≤: {musicAnalysis.preferences.total_tracks_analyzed}
                </p>
              </div>
            </div>
          )}

          {/* –¢–æ–ø —Ç—Ä–µ–∫–∏ */}
          {topTracks.length > 0 && hasData && (
            <div className="dashboard-block fade-in slide-up" style={{ background: 'var(--bg-card)', padding: '20px', borderRadius: '10px', marginBottom: '20px' }}>
              <h2>üî• –í–∞—à–∏ —Ç–æ–ø —Ç—Ä–µ–∫–∏</h2>
              <div style={{ display: 'grid', gridTemplateColumns: 'repeat(auto-fit, minmax(250px, 1fr))', gap: '15px' }}>
                {topTracks.map((track, index) => (
                  <div key={index} style={{ background: 'white', padding: '15px', borderRadius: '8px', display: 'flex', alignItems: 'center' }}>
                    {track.image && (
                      <img 
                        src={track.image} 
                        alt={track.name}
                        style={{ width: '60px', height: '60px', borderRadius: '5px', marginRight: '15px' }}
                      />
                    )}
                    <div>
                      <h4 style={{ margin: '0 0 5px 0' }}>{track.name}</h4>
                      <p style={{ margin: '0', color: '#666' }}>{track.artist}</p>
                      <div style={{ fontSize: '12px', color: '#999', marginTop: '5px' }}>
                        <span>üòä {formatPercentage(track.valence)}%</span> ‚Ä¢ 
                        <span>‚ö° {formatPercentage(track.energy)}%</span> ‚Ä¢ 
                        <span>üíÉ {formatPercentage(track.danceability)}%</span>
                      </div>
                    </div>
                  </div>
                ))}
              </div>
            </div>
          )}

          {/* –û—à–∏–±–∫–∞ */}
          {error && (
            <div className="dashboard-block fade-in slide-up" style={{ background: '#f8d7da', border: '1px solid #f5c6cb', padding: '20px', borderRadius: '10px', marginBottom: '20px', color: '#b30000', width: '100%' }}>
              <h3>‚ùå –û—à–∏–±–∫–∞</h3>
              <p>{error}</p>
            </div>
          )}
        </div>
      )}

      {activeTab === 'chat' && (
        <div style={{ height: 'calc(100vh - 200px)', minHeight: '500px' }}>
          <Chat userPreferences={musicAnalysis} />
        </div>
      )}
    </div>
  )
}

export default Dashboard 